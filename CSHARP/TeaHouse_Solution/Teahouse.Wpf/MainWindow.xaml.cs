using System.Windows.Shapes;

namespace Teahouse.Wpf
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Window_Loaded(object sender, RoutedEventArgs e)
        {
            Messenger.Default.Register<string>(this, "ExtraResult", msg => {
                (DataContext as MainViewModel).LoadCmd.Execute(null);
                MessageBox.Show(msg);
            });
            (DataContext as MainViewModel).EditorFunc = (extra) => {
                EditorWindow editorWindow = new EditorWindow();
                editorWindow.DataContext = extra;
                return (editorWindow.ShowDialog() == true);
            };
        }

        private void Window_Closing(object sender, System.ComponentModel.CancelEventArgs e)
        {
            Messenger.Default.Unregister(this);
        }
    }
}
